#include <unistd.h>

int		main(int argc, char **argv)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	if (argc == 2)
	{
		while (argv[1][i] != '\0')
		{
			if (argv[1][i] == 'a')
				j++;
			i++;
		}
		if (j > 0)
			write(1, "a\n", 2);
		else
			write(1, "\n", 1);
	}
	else
		write(1, "a\n", 2);
	return (0);
}

