#include "libft.h"

size_t	ft_charcount(char const *s, char c)
{
	size_t	i;
	size_t	nb;

	i = 0;
	nb = 0;
	while (s[i] != '\0')
	{
		if (s[i] == c)
			nb++;
		i++;
	}
	return (nb);
}
