#include <string.h>

char	*ft_strncpy(char *dst, const char *src, size_t n)
{
	size_t	i;
	char	*s;

	s = dst;
	i = 0;
	while (*src != '\0' && n > 0)
	{
		*s++ = *src++;
		n--;
	}
	while (n > 0)
	{
		*s++ = '\0';
		n--;
	}
	return (dst);
}
